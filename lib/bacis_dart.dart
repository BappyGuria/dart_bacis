void main() {
  const int a = 15;
  const int b = 7;
// Addition
  int Addition = a + b;
  print('The sum of a and b is: $Addition');
// Subtraction
  int Subtraction = a - b;
  print('The Subtraction between a and b is: $Subtraction');
// Multiplication
  int Multiplication = a * b;
  print('The Multiplication of a and b is: $Multiplication');
// Division
  double Division = a / b; // Using double to get a decimal result
  print('The Division of a and b is: $Division');
// Modulus
  int Modulus = a % b;
  print('The Modulus of a and b is: $Modulus');
}